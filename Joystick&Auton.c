//driver control

void setleft (int speed){
	motor[l_drive]=speed;
}

void setright (int speed){
	motor[r_drive]=speed;
}

void puncher () {
	if (vexRT[Btn5DXmtr2] == 1)      //If button 5D is pressed...
	{
		motor[l_puncher] = 127; 	//...raise the arm.
		motor[r_puncher] = 127;
	}
	else if(vexRT[Btn5UXmtr2] == 1)  //Else, if button 5U is pressed...
	{
		motor[l_puncher] = -127; 	//...lower the arm.
		motor[r_puncher] = -127;
	}
	else                      	//Else (neither button is pressed)...
	{
		motor[l_puncher] = 0;     //...stop the arm.
		motor[r_puncher] = 0;
	}
}
void tanktread () {
	if (vexRT[Btn6U] == 1)      //If button 6U is pressed...
	{
		motor[tread] = 127; 	//... the claw spin
	}

}
void cascadesecond (int speed) {
	motor[l_cascade_bot] = speed;       // when partner joystick channel 3 on moves second stage moves
	motor[r_cascade_bot] = speed;
}
void cascadefirst (int speed) {
	motor[r_cascade_top] = speed;  //when partner joystick channel 2 moves third stage moves
	motor[l_cascade_bot] = speed;
}
void clawflip () {
	if (vexRT[Btn6UXmtr2] == 1)      //If button 6U is pressed...
	{
		motor[claw] = 50; 	//... the claw spin
	}
	else if(vexRT[Btn6DXmtr2] == 1)  //Else, if button 6D is pressed...
	{
		motor[claw] = -40; 	//...claw spins other direction
	}
	else                      	//Else (neither button is pressed)...
	{
		motor[claw] = 0;     //...stop the claw
	}
}

void joystick()
{
while (1==1){
	motor[l_drive] = vexRT[Ch3];
	motor[r_drive] = vexRT[Ch2];
	puncher ();
	tanktread ();
	cascadesecond ( vexRT[Ch3Xmtr2]);  //all on partner joystick
	cascadefirst (vexRT[Ch2Xmtr2]);
	clawflip ();
}
}
